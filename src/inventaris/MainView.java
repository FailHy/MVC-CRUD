    public void ambildatapertamakali(){
        DefaultTableModel tabelnya = (DefaultTableModel) tabelsemua.getModel();
         DatabaseModel koneksi = new DatabaseModel();

        List<Object[]> dataDariDatabase = koneksi.bacadata();

        // Kosongkan tabel sebelum memasukkan data baru
        kosongkantabel();

        for (Object[] data : dataDariDatabase) {
            tabelnya.addRow(data);
        }
    }
    
    public void masukinketabel(Object[] datanya){
        
        //membuat objek tabel untuk persiapan memasukkan data
        DefaultTableModel tabelnya = (DefaultTableModel) tabelsemua.getModel();
        
        //memasukkan data dari databes ke tabel
        tabelnya.addRow(datanya);
    }
    
