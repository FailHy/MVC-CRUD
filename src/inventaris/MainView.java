    private void btnMasukkanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMasukkanActionPerformed
        // TODO add your handling code here:
        
         DatabaseModel koneksi = new DatabaseModel();

        int nomorbuku = Integer.parseInt(boxNomorBuku.getText());
        String judulbuku = boxJudulBuku.getText();
        String pengarang = boxpengarang.getText();
        int tahunterbit = Integer.parseInt(boxTahunTerbit.getText());

        // Memeriksa apakah data sudah ada sebelum menambahkan data
        if (!koneksi.cekData(nomorbuku)) {
            Object[] datanya = {nomorbuku, judulbuku, pengarang, tahunterbit};
            koneksi.tambahdata(nomorbuku, judulbuku, pengarang, tahunterbit);
            kosongkantabel();
            ambildatapertamakali();
        } else {
            datasudahada(nomorbuku);
        }
    }//GEN-LAST:event_btnMasukkanActionPerformed

    private void btnPerbaruiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPerbaruiActionPerformed
        // TODO add your handling code here:
         DatabaseModel koneksi = new DatabaseModel();

        int nomorbuku = Integer.parseInt(boxNomorBuku.getText());
        String judulbuku = boxJudulBuku.getText();
        String pengarang = boxpengarang.getText();
        int tahunterbit = Integer.parseInt(boxTahunTerbit.getText());

        // Memeriksa apakah data sudah ada sebelum melakukan update
        if (koneksi.cekData(nomorbuku)) {
            koneksi.updatedata(nomorbuku, judulbuku, pengarang, tahunterbit);
            kosongkantabel();
            ambildatapertamakali();
        } else {
            JOptionPane.showMessageDialog(null, "Data dengan Nomor Buku " + nomorbuku + " tidak ditemukan di database.");
        }
    }//GEN-LAST:event_btnPerbaruiActionPerformed

